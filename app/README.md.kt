# Reader App - Android

## Introduction

Welcome to Reader App, an Android application designed to enhance your reading experience and provide a seamless way to discover, organize, and read your favorite books. With Reader App, you can easily search for books using the Google Books API, create a personal library, start reading, tag books, and add personal notes to make your reading journey more enjoyable.

## Features
- **User Login**:
Securely log in to your Reader App account to access your saved library and personalized preferences.

- **Search Books**:
Utilize the Google Books API to search for books by title, author, or keywords to find the books you want to read.

- **Add to Library**:
Save your favorite books to your personal library for easy access and organization.

- **Start Reading**:
Open any book from your library and start reading right within the app.

- **Tagging Books**:
Organize your books by adding tags like "Android," "Flutter" etc., to quickly find and categorize your collection.

- **Current Reading List**:
Keep track of the books you are currently reading with a dedicated "Reading Now" list.

- **Adding Notes**:
Add personal notes to books, including thoughts, quotes, or reflections to enrich your reading experience.

## Technology Used
The Reader App for Android is built using the following technologies and tools:
- **Kotlin**:
I use Kotlin, the modern and official language for Android development, to build the Android application.

- **Jetpack Compose**:
Reader App leverages Jetpack Compose, the declarative UI toolkit for building native Android UIs. It enables us to create interactive and dynamic user interfaces efficiently.

- **Android Studio**:
The app is developed and managed using Android Studio, the official IDE for Android app development.

- **Google Books API**:
Reader App integrates with the Google Books API to fetch book data and cover images.

- **Firebase Firestore**:
We use Firebase Firestore, a cloud-based NoSQL database, to store user data, book information, and notes securely.

- **Firebase Authentication**:
User authentication is implemented using Firebase Authentication to ensure secure user logins and protect user data and privacy.

## How to Clone and Build the App

To clone and build the Reader App for Android on your development environment, follow these steps:
1. **Prerequisites**:
- Install Android Studio on your system. You can download it from the official Android Studio website.

2. **Clone the Repository**:
- Clone the Reader App repository from our GitHub page using the following command:
  git clone https://github.com/AyubuAyubu/ReaderAppDroid

3. **Open the Project**:
- Open Android Studio and select "Open an existing Android Studio project."
- Navigate to the location where you cloned the Reader App repository and select the "android-app" folder.

4. **Build and Run**:
- Connect your Android device to your computer, or set up an Android Virtual Device (AVD) in Android Studio.
- Build the app by clicking the "Build" button in the Android Studio toolbar.
- After a successful build, click the "Run" button to install the app on your connected device or AVD.

5. **Explore the App**:
- Once the app is installed, launch it on your Android device.
- Log in or create a new account to start using the Reader App and explore its features.
